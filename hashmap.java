import java.util.*;
public class hashmap {
    public static void main(String[] args) {
        HashMap<Integer,String> a = new HashMap<>();
        a.put(32,"Jordan");
        a.put(30, "curry");
        a.put(34, "giannis");
        System.out.println(a); // {32=Jordan, 34=giannis, 30=curry}
        
    }
}
